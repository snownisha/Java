package A031_ASCIIValue;

public class ASCII {
    public static void main(String[] args)
    {
        char ch1 = 's';
        char ch2 = 'n';
        char ch3 = 'o';

        int asciival1 = ch1;
        int asciival2 = ch2;
        int asciival3 = ch3;
        System.out.println("The ASCII value of " + ch1 + " is: " + asciival1);
        System.out.println("The ASCII value of " + ch2 + " is: " + asciival2);
        System.out.println("The ASCII value of " + ch2 + " is: " + asciival3);
    }
}

